
package Employee;

public class EmployeeType
{
    private long Id;
    private String Name;
    
    //Getters & Setters
    public long getId()
    {
        return Id;
    }
    public void setId(long Id)
    {
        this.Id = Id;
    }

    public String getName()
    {
        return Name;
    }
    public void setName(String Name)
    {
        this.Name = Name;
    }
}
